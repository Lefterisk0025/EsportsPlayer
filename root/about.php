    <h3 style="font-size:35px; padding-top:50px; text-align:center;">About Rekkles</h3>

    <h4 style="font-size: 25px; padding-left:15px;">The Beginning</h4>
    <p style="font-size: 20px;"> &nbsp;&nbsp;Rekkles started playing competitive League of Legends in 2012, playing for Playing Ducks and PAH,
    <img id='RekklesWarmUpImg' src="Images/RekklesWarmUp.jpg" alt="RekklesWarmUp"> 
    whilst also subbing for both Team BLACK and SK Gaming. During this time, even at such a young age, 
    Rekkles had the chance to play against teams of an especially high standard. 
    In November 2012, he joined Fnatic as their starting AD Carry.<br><br>
    &nbsp;&nbsp; Rekkles' young age had limited him however, preventing him from being able to play in the 2013 EU LCS for Fnatic.
    As he was unable to play in the LCS, Fnatic.Beta (a secondary Fnatic squad) was created around him. However the team disbanded after a few months. In May 2013 he started playing together with YoungBuck, Shook v2, 
    cowTard, and Unlimited as PrideFC, and after the team got signed by Copenhagen Wolves, he agreed to continue playing with them as a stand-in. Together, he and Fnatic decided that it would be best for 
    him to rejoin the roster in the 2014 Season, as he was not eligible to play until after the group stage.
    On November 22, 2013, Rekkles officially retook the starting AD Carry position from puszu.</p>

    <h4 style="font-size: 25px; padding-left:15px;">The Rise of a Legend</h4>
    <p style="font-size: 20px;">&nbsp;&nbsp;Rekkles and Fnatic started off their season at the IEM Season VIII - World Championship. The team came 2nd, losing to KT Rolster Bullets in the Grand Final.
    The Spring Split went relatively well for the team. Rekkles' very first week playing in the Spring Split saw 
    him take the prize of weekly MVP as Fnatic won each of their 4 superweek games, mainly due to his performances on Jinx. 
    Fnatic ended the split in 2nd place behind SK Gaming, securing themselves a place in the Spring Playoffs. The whole team raised their game for the playoffs, 
    and they emerged victorious after a win against Alliance in their semifinal matchup, and against SK Gaming in the Grand Final.
    Due to Fnatic's performance in the Spring Playoffs, they had qualified to compete at All-Star Paris 2014, along with other regional winners from around the world. 
    The team made it to the semifinals of the tournament, losing to the world champions at the time, SK Telecom T1 K.<br><br>
    &nbsp;&nbsp;The start of the Summer Split was shaky for Rekkles and the team. Fnatic could not break in to the top 2 until Week 7 of the split, 
    at which point their main rivals Alliance were already a considerable distance ahead of them in the race for 1st place. Rekkles managed to impress with his Lucian and Vayne throughout, 
    picking up the Weekly MVP Award for Week 9, though Fnatic could only manage a 2nd place finish. The team aimed to put the split behind them and focus on the Summer Playoffs. 
    A close fought win over ROCCAT in the semifinal meant that Rekkles and the team would face Alliance in the final. 
    The match ended in defeat for Fnatic, but the playoffs saw the team seal their qualification for the 2014 Season World Championship.
    Fnatic were placed with Group C along with LMQ, OMG and Samsung Blue. Fnatic only managed 2 wins in their group, meaning that Rekkles and the team did not progress to the knockout stage.<br><br>

    &nbsp;&nbsp;On November 24, 2014, Rekkles officially joined Alliance as starting AD carry, after long-standing rumors that the move was going to happen. 
    His first event with Alliance was IEM San Jose. Seeded directly into the semifinals, Rekkles and the team lost their matchup to Cloud9 and tied for third place with Team SoloMid. 
    Rekkles was nominated for the MVP award along with PowerOfEvil, Vizicsacsi, Kikis, and Sneaky but lost the vote to Sneaky.
    In order to comply with new LCS regulations, Alliance would have to change their name for the upcoming season. 
    On January 8, it was announced that the team had rebranded as Elements. The Spring Split itself was not a success for the team, finishing in 7th place after making a number of roster changes. 
    This meant that the team would not go to playoffs, but would qualify for the Summer Split.<br><br>
    &nbsp;&nbsp;On May 14, Rekkles rejoined Fnatic. The team went into the Summer Season with confidence running high after their performance 
    <img id='RekklesThink' src="Images/RekklesThink.jpg" alt="RekklesThink">
    on the international stage at the 2015 Mid-Season Invitational, with Steeelback on the roster, before Rekkles rejoined the team. 
    Fnatic managed to finish the regular season with a perfect, unbeaten 18-0 record - and became the first team to achieve this in League Championship Series history. 
    This meant a 1st place regular season finish for the team and a bye to the semifinals of the Summer Playoffs. 
    Fnatic went on to win the playoffs, beating Origen 3-2 in a tight final. This qualified the team for the 2015 World Championship, where they would compete as Europe's #1 seed.
    At the 2015 World Championship, Fnatic were seeded into Group B along with ahq e-Sports Club, Invictus Gaming, and Cloud9. 
    They picked up 4 wins in the group, going unbeaten in the 2nd week of group stage games, thanks in part to Rekkles' Kennen pick, meaning they would advance to the knockout stage of the tournament. 
    In the quarterfinals, Fnatic beat EDward Gaming 3-0 before suffering a 3-0 loss to KOO Tigers in the semifinals, putting an end to their campaign.<br><br><br>

    &nbsp;&nbsp;At the start of the season 2016, Fnatic were voted to play at IEM Season X - Cologne. 
    They fielded a roster including Rekkles and Febiven from their 2015 roster along with new members Gamsu, Spirit, and NoXiAK. 
    The team had a bye to the semifinals, but were beaten at this stage by Qiao Gu Reapers. 
    Despite being only in fifth place in the EU LCS split at the time of the tournament, Fnatic were invited to IEM Katowice due to their previous season's success. 
    There, they had a surprisingly strong run, finishing in second place behind SK Telecom. 
    Despite his only being played one time in premier-region play prior to Katowice (by Sneaky on Cloud9 in North America), 
    Rekkles played Jhin in 5 out of Fnatic's 6 wins, not dropping a single game on the champion.
